#include <iostream>
using namespace std;

int main() {
    int number = 5;
    if (number > 0) {
        cout << "The number is positive." << endl;
    } else {
        cout << "The number is not positive." << endl;
    }

    int i = 1;
    while (i <= 5) {
        cout << i << " ";
        i++;
    }

    return 0;
}
